#!/usr/bin/env node
/**
 * sync-pricing-engine.js
 *
 * Copies web-app/src/utils/pricing-engine.ts → supabase/functions/calc-tax-engine/pricing-engine.ts
 * and verifies checksum equality.
 *
 * Usage:
 *   node scripts/sync-pricing-engine.js          # copy & verify
 *   node scripts/sync-pricing-engine.js --check  # verify only (for CI)
 */
const fs = require('fs')
const path = require('path')
const crypto = require('crypto')

const SOURCE = path.resolve(__dirname, '..', 'src', 'utils', 'pricing-engine.ts')
const TARGET = path.resolve(__dirname, '..', 'supabase', 'functions', 'calc-tax-engine', 'pricing-engine.ts')

const HEADER = `/**
 * pricing-engine.ts — Single source of truth for price calculation.
 *
 * ALL percentages are decimals in range 0-1 (e.g. 0.10 = 10%).
 * The UI layer converts display values before calling this module.
 *
 * This module is pure (no I/O, no framework deps, no Supabase).
 * It is shared between web-app (preview) and edge function (persist).
 *
 * >>> DO NOT EDIT THIS FILE DIRECTLY <<<
 * Source of truth: web-app/src/utils/pricing-engine.ts
 * Sync with: npm run sync-pricing-engine
 */`

function sha256(content) {
  return crypto.createHash('sha256').update(content).digest('hex')
}

function getLogicContent(filePath) {
  const content = fs.readFileSync(filePath, 'utf-8')
  // Strip the first JSDoc block to compare only the logic
  return content.replace(/^\/\*\*[\s\S]*?\*\/\s*/, '').trim()
}

const checkOnly = process.argv.includes('--check')

if (!fs.existsSync(SOURCE)) {
  console.error(`Source not found: ${SOURCE}`)
  process.exit(1)
}

const sourceContent = fs.readFileSync(SOURCE, 'utf-8')
const sourceLogic = getLogicContent(SOURCE)

if (checkOnly) {
  if (!fs.existsSync(TARGET)) {
    console.error(`Target not found: ${TARGET}`)
    console.error('Run "npm run sync-pricing-engine" to create it.')
    process.exit(1)
  }
  const targetLogic = getLogicContent(TARGET)
  const sourceHash = sha256(sourceLogic)
  const targetHash = sha256(targetLogic)

  if (sourceHash !== targetHash) {
    console.error('DRIFT DETECTED: pricing-engine.ts in edge function differs from source.')
    console.error(`  Source: ${sourceHash.slice(0, 12)}...`)
    console.error(`  Target: ${targetHash.slice(0, 12)}...`)
    console.error('Run "npm run sync-pricing-engine" to fix.')
    process.exit(1)
  }

  console.log('pricing-engine.ts: checksums match.')
  process.exit(0)
}

// Copy mode: replace header then write
const logicOnly = sourceContent.replace(/^\/\*\*[\s\S]*?\*\/\s*/, '').trim()
const output = HEADER + '\n\n' + logicOnly + '\n'

fs.mkdirSync(path.dirname(TARGET), { recursive: true })
fs.writeFileSync(TARGET, output, 'utf-8')
console.log(`Synced: ${path.relative(process.cwd(), SOURCE)} → ${path.relative(process.cwd(), TARGET)}`)
console.log(`Checksum: ${sha256(logicOnly).slice(0, 12)}...`)
